# Array (list) for storing order items
order_items = ["item1", "item2", "item3", "item4", "item5"]

# Accessing an item by its index (position)
print("First item in the order:", order_items[0])  # Outputs: item1

# Adding a new item to the order
order_items.append("item6")
print("Updated order items:", order_items)

# Removing an item from the order
order_items.remove("item3")
print("Order after removing an item:", order_items)
